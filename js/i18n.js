export function t(lang, key, ...args) {
  const entry = (dict[lang] && dict[lang][key]) ?? dict.fr[key];
  return typeof entry === 'function' ? entry(...args) : entry;
}

export function applyTranslations(lang) {
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    el.innerHTML = t(lang, el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.placeholder = t(lang, el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-aria-label]').forEach((el) => {
    el.setAttribute('aria-label', t(lang, el.dataset.i18nAriaLabel));
  });
}

export function buildLangSwitcher(currentLang, onChange) {
  let selected = currentLang;

  const wrapper = document.createElement('div');
  wrapper.className = 'lang-switcher';

  const trigger = document.createElement('button');
  trigger.type = 'button';
  trigger.className = 'lang-switcher-trigger';
  trigger.setAttribute('aria-haspopup', 'listbox');
  trigger.setAttribute('aria-expanded', 'false');
  trigger.setAttribute('aria-label', 'Langue / Language / Limbă');
  trigger.innerHTML = `<span class="lang-switcher-icon" aria-hidden="true"><svg viewBox="0 0 24 24" width="15" height="15" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><circle cx="12" cy="12" r="9"/><path d="M3 12h18"/><path d="M12 3a14 14 0 0 1 0 18"/><path d="M12 3a14 14 0 0 0 0 18"/></svg></span><span class="lang-switcher-current">${LANG_LABELS[selected]}</span><span class="lang-switcher-current-short">${selected.toUpperCase()}</span><span class="lang-switcher-chevron">&#9662;</span>`;

  const menu = document.createElement('ul');
  menu.className = 'lang-switcher-menu';
  menu.setAttribute('role', 'listbox');
  menu.hidden = true;

  function handleOutsideClick(e) {
    if (!wrapper.contains(e.target)) closeMenu();
  }

  function openMenu() {
    menu.hidden = false;
    trigger.setAttribute('aria-expanded', 'true');
    document.addEventListener('click', handleOutsideClick);
  }

  function closeMenu() {
    menu.hidden = true;
    trigger.setAttribute('aria-expanded', 'false');
    document.removeEventListener('click', handleOutsideClick);
  }

  function renderOptions() {
    menu.innerHTML = '';
    LANGS.forEach((code) => {
      const li = document.createElement('li');
      li.className = 'lang-switcher-option';
      li.setAttribute('role', 'option');
      li.dataset.lang = code;
      if (code === selected) li.setAttribute('aria-selected', 'true');
      li.innerHTML = `<span class="check">${code === selected ? '&#10003;' : ''}</span><span>${LANG_LABELS[code]}</span>`;
      li.addEventListener('click', () => {
        if (code !== selected) {
          selected = code;
          trigger.querySelector('.lang-switcher-current').textContent = LANG_LABELS[code];
          trigger.querySelector('.lang-switcher-current-short').textContent = code.toUpperCase();
          renderOptions();
          onChange(code);
        }
        closeMenu();
      });
      menu.appendChild(li);
    });
  }

  trigger.addEventListener('click', () => {
    if (menu.hidden) openMenu();
    else closeMenu();
  });

  renderOptions();
  wrapper.appendChild(trigger);
  wrapper.appendChild(menu);
  return wrapper;
}
